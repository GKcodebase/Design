/**
 * The enum Game status.
 */
public enum GameStatus {
    /**
     * Won game status.
     */
    WON,
    /**
     * Lost game status.
     */
    LOST,
    /**
     * Draw game status.
     */
    DRAW,
    /**
     * Cancelled game status.
     */
    CANCELLED ,
    /**
     * Playing game status.
     */
    PLAYING
}
