package NULLObjectDesign;

/**
 * The interface Employee.
 */
interface Employee {
    /**
     * Gets name.
     *
     * @return the name
     */
    String getName();

    /**
     * Gets role.
     *
     * @return the role
     */
    String getRole();

    /**
     * Is null boolean.
     *
     * @return the boolean
     */
    boolean isNull();
}


